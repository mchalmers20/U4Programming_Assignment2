public interface Actions { // The methods would be called to perform actions here

    public void add(); // Adds a player with relavent data

    public void view(); // Would show a list of all players in a team

    public void remove(); // Would remove a player and their data

    public void setFirstName(); // Players first name would be applied

    public void setLastName(); // Players last name would be applied

    public void setTeam(); // Players would be added to a selected team

    public void addToArrayList(); // Add a team to the list of current teams
}
